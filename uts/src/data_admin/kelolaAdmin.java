    private void hapusData(int baris){
    Connection con;
    Statement st;
    try {
    con = Koneksi.Sambung();
    st = con.createStatement();
    st.executeUpdate("DELETE FROM userpass WHERE id="+baris);
    st.close();
    con.close();
    resetView();
    JOptionPane.showMessageDialog(this, "Data telah dihapus");
    } catch (SQLException e) {
    JOptionPane.showMessageDialog(this, e.getMessage());
  }
}
    private void pilihData(String n){
    hapus.setEnabled(true);
    String sql = "SELECT * FROM userpass WHERE id='"+n+"'";
    Connection con;
    Statement st;
    ResultSet rs;
    try {
    con = Koneksi.Sambung();
    st = con.createStatement();
    rs = st.executeQuery(sql);
    while (rs.next()) {
    int id = rs.getInt(1);
    String name = rs.getString(2);
    String passwd = rs.getString(3);

    idBaris = id;
    txt_username.setText(name);
    txt_pass.setText(passwd);
}
    st.close();
    con.close();
    showForm(true);
    } catch (SQLException e) {
    System.err.println("pilihData(): "+e.getMessage());
}
        }
