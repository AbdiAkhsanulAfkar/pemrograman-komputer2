    private void showData(String key){
    model.getDataVector().removeAllElements();
    String where = "";
    if(!key.isEmpty()){
    where += "WHERE username LIKE '%"+key+"%' "
    + "OR pass LIKE '%"+key+"%' ";
}
    String sql = "SELECT * FROM userpass "+where;
    Connection con;
    Statement st;
    ResultSet rs;
    int baris = 0;
    try {
    con = data_admin.Koneksi.Sambung();
    st = con.createStatement();
    rs = st.executeQuery(sql);
    while (rs.next()) {
    Object id = rs.getInt(1);
    Object name = rs.getString(2);
    Object pass = rs.getString(3);

    Object[] data = {id,name,pass};

    model.insertRow(baris, data);

    baris++;

}
    st.close();
    con.close();
    tabel.revalidate();
    tabel.repaint();
    } catch (SQLException e) {
    System.err.println("showData(): "+e.getMessage());
}
}
